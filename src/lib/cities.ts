/* ─────────────────────────────────────────────────────────────────
   City Landing Page Data
   每個城市一份設定，page.tsx 透過 generateStaticParams 靜態生成
───────────────────────────────────────────────────────────────── */

export interface CityDistrict {
  name: string;   // 區名
  note: string;   // 商業特性簡述
}

export interface CityFAQ {
  q: string;
  a: string;
}

export interface CityData {
  /** URL slug — also used in canonical */
  slug: 'taipei' | 'taichung' | 'kaohsiung';
  /** 城市中文名 */
  name: string;
  /** 城市英文名 */
  nameEn: string;
  /** 行政區（for schema areaServed） */
  region: string;
  /** 頁面 title（30–60 chars） */
  metaTitle: string;
  /** Meta description（120–160 chars） */
  metaDescription: string;
  /** H1 主標題 */
  h1: string;
  /** H1 副標（hero subtitle） */
  subtitle: string;
  /** 城市特色一句話 */
  marketProfile: string;
  /** 競爭程度描述 */
  competitionNote: string;
  /** 主要商圈 / 覆蓋行政區 */
  districts: CityDistrict[];
  /** 城市特有痛點（3 條） */
  painPoints: { title: string; desc: string }[];
  /** 城市特有成效數字（3 組） */
  stats: { value: string; label: string }[];
  /** FAQ（5 題以上） */
  faqs: CityFAQ[];
  /** Unsplash cover image */
  coverImage: { url: string; alt: string };
}

